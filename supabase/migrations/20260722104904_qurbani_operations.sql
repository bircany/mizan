    grant usage on schema private to service_role;
    grant usage, select on sequence private.qurbani_order_number_seq to service_role;
    for sequence_name in
      select pg_get_serial_sequence(format('public.%I', table_name), 'id')
      from unnest(array[
        'qurbani_seasons', 'qurbani_seasons_locales',
        'qurbani_products', 'qurbani_products_locales',
        'qurbani_pools', 'qurbani_orders', 'qurbani_shares', 'qurbani_videos',
        'qurbani_access_links', 'qurbani_access_links_rels', 'qurbani_messages', 'qurbani_jobs'
      ]) table_name
    loop
      execute format('grant usage, select on sequence %s to service_role', sequence_name);
    end loop;
    grant execute on function private.qurbani_refresh_pool_counters(integer) to service_role;
    grant execute on function private.qurbani_finalize_pool_code(integer) to service_role;
    grant execute on function private.qurbani_reserve_order(jsonb) to service_role;
    grant execute on function private.qurbani_confirm_order_payment(integer, integer, text, text) to service_role;
    grant execute on function private.qurbani_expire_reservations() to service_role;
    grant execute on function private.qurbani_transfer_order(integer, integer, integer) to service_role;
  end if;
end;
$$;

commit;
