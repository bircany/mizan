create or replace function private.qurbani_refresh_pool_counters(p_pool_id integer)
returns void
language plpgsql
security invoker
set search_path = pg_catalog, public, private
as $$
declare
  v_reserved numeric;
  v_confirmed numeric;
  v_capacity numeric;
  v_status public.enum_qurbani_pools_status;
  v_locked_at timestamptz;
begin
  select capacity, status, locked_at
    into v_capacity, v_status, v_locked_at
  from public.qurbani_pools
  where id = p_pool_id
  for update;

  if not found then
    raise exception 'Qurbani pool % not found', p_pool_id;
  end if;

  select
    coalesce(sum(share_count) filter (
      where status in ('reserved', 'pending_payment', 'pending_eft_review') and reserved_until > now()
    ), 0),
    coalesce(sum(share_count) filter (where status = 'paid'), 0)
  into v_reserved, v_confirmed
  from public.qurbani_orders
  where pool_id = p_pool_id;

  if v_reserved + v_confirmed > v_capacity then
    raise exception 'Pool % capacity exceeded', p_pool_id;
  end if;

  update public.qurbani_pools
  set reserved_count = v_reserved,
      confirmed_count = v_confirmed,
      status = case
        when v_confirmed = v_capacity and status = 'open' then 'full'::public.enum_qurbani_pools_status
        when v_confirmed < v_capacity and status = 'full' and v_locked_at is null then 'open'::public.enum_qurbani_pools_status
        else status
      end,
      full_at = case when v_confirmed = v_capacity then coalesce(full_at, now()) else full_at end,
      updated_at = now()
  where id = p_pool_id;
end;
$$;

create or replace function private.qurbani_finalize_pool_code(p_pool_id integer)
returns character varying
language plpgsql
security invoker
set search_path = pg_catalog, public, private
as $$
declare
  v_season_id integer;
  v_year integer;
  v_counter integer;
  v_code character varying;
  v_capacity integer;
  v_confirmed integer;
begin
  select p.season_id, p.capacity::integer, p.confirmed_count::integer, p.code
    into v_season_id, v_capacity, v_confirmed, v_code
  from public.qurbani_pools p
  where p.id = p_pool_id
  for update;

  if not found then raise exception 'Qurbani pool % not found', p_pool_id; end if;
  if v_confirmed <> v_capacity then return v_code; end if;
  if v_code is not null then return v_code; end if;

  update public.qurbani_seasons
  set code_counter = code_counter + 1, updated_at = now()
  where id = v_season_id
  returning year::integer, code_counter::integer into v_year, v_counter;

  if v_counter > 9999 then raise exception 'Qurbani code capacity exhausted for season %', v_year; end if;
  v_code := format('MD-%s-%s', v_year, lpad(v_counter::text, 4, '0'));

  update public.qurbani_pools
  set code = v_code,
      status = case when status = 'open' then 'full'::public.enum_qurbani_pools_status else status end,
      full_at = coalesce(full_at, now()),
      updated_at = now()
  where id = p_pool_id;

  return v_code;
end;
$$;

create or replace function private.qurbani_reserve_order(p_order jsonb)
returns table (
  order_id integer,
  pool_id integer,
  reserved_until timestamp with time zone,
  total_amount numeric,
  currency text
)
language plpgsql
security invoker
set search_path = pg_catalog, public, private
as $$
declare
  v_product public.qurbani_products%rowtype;
  v_season public.qurbani_seasons%rowtype;
  v_pool_id integer;
  v_order_id integer;
  v_share_count integer;
  v_reserved_until timestamptz;
  v_order_number text;
  v_payment_method public.enum_qurbani_orders_payment_method;
  v_order_status public.enum_qurbani_orders_status;
  v_buyer_name text := btrim(coalesce(p_order->>'buyerName', ''));
  v_buyer_email text := btrim(coalesce(p_order->>'buyerEmail', ''));
  v_buyer_phone text := btrim(coalesce(p_order->>'buyerPhone', ''));
  v_has_owner_phone boolean;
begin
  if jsonb_typeof(p_order) <> 'object' or jsonb_typeof(p_order->'shares') <> 'array' then
    raise exception 'Invalid qurbani order payload';
  end if;
  if v_buyer_name = '' or v_buyer_email = '' or v_buyer_phone = '' then
    raise exception 'Buyer name, email and phone are required';
  end if;
  if not (coalesce((p_order->>'proxyAccepted')::boolean, false) or p_order ? 'proxyAcceptedAt') then
    raise exception 'Digital proxy acceptance is required';
  end if;
  if not (coalesce((p_order->>'kvkkAccepted')::boolean, false) or p_order ? 'kvkkAcceptedAt') then
    raise exception 'KVKK acceptance is required';
  end if;
  if not (coalesce((p_order->>'termsAccepted')::boolean, false) or p_order ? 'termsAcceptedAt') then
    raise exception 'Terms acceptance is required';
  end if;

  v_share_count := jsonb_array_length(p_order->'shares');
  if v_share_count < 1 or v_share_count > 7 then raise exception 'Share count must be between 1 and 7'; end if;
  if exists (
    select 1 from jsonb_array_elements(p_order->'shares') share
    where btrim(coalesce(share->>'ownerName', '')) = ''
  ) then
    raise exception 'Every share owner name is required';
  end if;

  select exists (
    select 1 from jsonb_array_elements(p_order->'shares') share
    where btrim(coalesce(share->>'ownerPhone', '')) <> ''
  ) into v_has_owner_phone;
  if v_has_owner_phone and not (
    coalesce((p_order->>'thirdPartyContactConsent')::boolean, false)
    or p_order ? 'thirdPartyContactConsentAt'
  ) then
    raise exception 'Third-party contact consent is required';
  end if;

  select * into v_product
  from public.qurbani_products
  where id = (p_order->>'productId')::integer
  for update;
  if not found then raise exception 'Qurbani product not found'; end if;

  select * into v_season
  from public.qurbani_seasons
  where id = v_product.season_id
  for update;

  if not v_product.is_active or v_season.status <> 'active' then
    raise exception 'Qurbani product is not on sale';
  end if;
  if now() < coalesce(v_product.sales_start_at, v_season.sales_start_at)
    or now() > coalesce(v_product.sales_end_at, v_season.sales_end_at) then
    raise exception 'Qurbani sales window is closed';
  end if;
  if v_share_count > v_product.capacity then
    raise exception 'The complete order does not fit in one animal pool';
  end if;

  begin
    v_payment_method := (p_order->>'paymentMethod')::public.enum_qurbani_orders_payment_method;
  exception when invalid_text_representation then
    raise exception 'Unsupported payment method';
  end;
  if v_payment_method is null then raise exception 'Payment method is required'; end if;

  v_reserved_until := now() + case when v_payment_method = 'iyzico' then interval '30 minutes' else interval '24 hours' end;
  v_order_status := case when v_payment_method = 'iyzico'
    then 'pending_payment'::public.enum_qurbani_orders_status
    else 'pending_eft_review'::public.enum_qurbani_orders_status end;

  select p.id into v_pool_id
  from public.qurbani_pools p
  where p.product_id = v_product.id
    and p.season_id = v_product.season_id
    and p.status = 'open'
    and p.locked_at is null
    and p.confirmed_count + p.reserved_count + v_share_count <= p.capacity
  order by p.id
  for update skip locked
  limit 1;

  if v_pool_id is null then
    insert into public.qurbani_pools (
      season_id, product_id, capacity, reserved_count, confirmed_count, status, updated_at, created_at
    ) values (
      v_product.season_id, v_product.id, v_product.capacity, 0, 0, 'open', now(), now()
    ) returning id into v_pool_id;
  end if;

  v_order_number := format(
    'KB-%s-%s',
    v_season.year::integer,
    lpad(nextval('private.qurbani_order_number_seq')::text, 8, '0')
  );

  insert into public.qurbani_orders (
    order_number, season_id, product_id, pool_id,
    buyer_name, buyer_email, buyer_phone, country_code, identity_type, identity_number,
    address_snapshot, product_snapshot, unit_price, share_count, total_amount, currency,
    payment_method, reservation_kind, status, reserved_until,
    proxy_text_version, proxy_accepted_at, proxy_accepted_ip,
    kvkk_accepted_at, terms_accepted_at, third_party_contact_consent_at,
    updated_at, created_at
  ) values (
    v_order_number, v_product.season_id, v_product.id, v_pool_id,
    v_buyer_name, v_buyer_email, v_buyer_phone,
    upper(coalesce(nullif(p_order->>'countryCode', ''), 'TR')),
    nullif(p_order->>'identityType', '')::public.enum_qurbani_orders_identity_type,
    nullif(btrim(p_order->>'identityNumber'), ''),
    coalesce(p_order->'addressSnapshot', '{}'::jsonb),
    jsonb_build_object(
      'productId', v_product.id, 'seasonId', v_product.season_id, 'kind', v_product.kind,
      'unitPrice', v_product.price, 'currency', v_product.currency, 'capacity', v_product.capacity
    ),
    v_product.price, v_share_count, v_product.price * v_share_count, v_product.currency::text::public.enum_qurbani_orders_currency,
    v_payment_method, v_payment_method::text::public.enum_qurbani_orders_reservation_kind, v_order_status, v_reserved_until,
    coalesce(nullif(p_order->>'proxyTextVersion', ''), v_season.proxy_text_version),
    now(), coalesce(nullif(p_order->>'ipAddress', ''), 'unknown'),
    now(), now(), case when v_has_owner_phone then now() else null end,
    now(), now()
  ) returning id into v_order_id;

  insert into public.qurbani_shares (
    order_id, pool_id, owner_name, owner_phone, effective_phone, sequence, status, updated_at, created_at
  )
  select
    v_order_id,
    v_pool_id,
    btrim(share.value->>'ownerName'),
    nullif(btrim(share.value->>'ownerPhone'), ''),
    coalesce(nullif(btrim(share.value->>'ownerPhone'), ''), v_buyer_phone),
    share.ordinality,
    'reserved'::public.enum_qurbani_shares_status,
    now(), now()
  from jsonb_array_elements(p_order->'shares') with ordinality as share(value, ordinality);

  update public.qurbani_pools
  set reserved_count = reserved_count + v_share_count, updated_at = now()
  where id = v_pool_id;

  insert into public.audit_logs(action, target_collection, target_id, details, ip_address, updated_at, created_at)
  values (
    'qurbani.order_reserved', 'qurbani-orders', v_order_id::text,
    jsonb_build_object('poolId', v_pool_id, 'shareCount', v_share_count, 'paymentMethod', v_payment_method),
    nullif(p_order->>'ipAddress', ''), now(), now()
  );

  return query select v_order_id, v_pool_id, v_reserved_until, v_product.price * v_share_count, v_product.currency::text;
end;
$$;

create or replace function private.qurbani_confirm_order_payment(
  p_order_id integer,
  p_donation_id integer default null,
  p_provider_id text default null,
  p_actor text default 'system'
)
returns table (pool_id integer, pool_code text, already_confirmed boolean)
language plpgsql
security invoker
set search_path = pg_catalog, public, private
as $$
declare
  v_order public.qurbani_orders%rowtype;
  v_product public.qurbani_products%rowtype;
  v_target_pool_id integer;
  v_source_pool_id integer;
  v_code text;
  v_pool record;
begin
  select * into v_order from public.qurbani_orders where id = p_order_id for update;
  if not found then raise exception 'Qurbani order % not found', p_order_id; end if;
  if v_order.status = 'paid' then
    select code into v_code from public.qurbani_pools where id = v_order.pool_id;
    return query select v_order.pool_id, v_code, true;
    return;
  end if;
  if v_order.status in ('cancelled', 'refunded', 'failed', 'action_required') then
    raise exception 'Qurbani order % cannot be confirmed from status %', p_order_id, v_order.status;
  end if;
  if p_provider_id is not null and exists (
    select 1 from public.qurbani_orders where provider_payment_id = p_provider_id and id <> p_order_id
  ) then
    raise exception 'Provider payment id is already bound to another qurbani order';
  end if;

  select * into v_product from public.qurbani_products where id = v_order.product_id for update;
  v_source_pool_id := v_order.pool_id;

  for v_pool in
    select id from public.qurbani_pools where product_id = v_order.product_id order by id for update
  loop
    perform private.qurbani_refresh_pool_counters(v_pool.id);
  end loop;

  select p.id into v_target_pool_id
  from public.qurbani_pools p
  where p.product_id = v_order.product_id
    and p.season_id = v_order.season_id
    and p.status = 'open'
    and p.locked_at is null
    and p.confirmed_count + p.reserved_count + v_order.share_count
      - case
          when p.id = v_source_pool_id
            and v_order.status in ('reserved', 'pending_payment', 'pending_eft_review')
            and v_order.reserved_until > now()
          then v_order.share_count
          else 0
        end <= p.capacity
  order by (p.id = v_source_pool_id) desc, p.id
  limit 1;

  if v_target_pool_id is null then
    insert into public.qurbani_pools(season_id, product_id, capacity, reserved_count, confirmed_count, status, updated_at, created_at)
    values(v_order.season_id, v_order.product_id, v_product.capacity, 0, 0, 'open', now(), now())
    returning id into v_target_pool_id;
  end if;

  update public.qurbani_orders
  set pool_id = v_target_pool_id,
      donation_id = coalesce(p_donation_id, donation_id),
      provider_payment_id = coalesce(nullif(p_provider_id, ''), provider_payment_id),
      provider_reference = coalesce(nullif(p_provider_id, ''), provider_reference),
      status = 'paid', paid_at = coalesce(paid_at, now()), failure_reason = null, updated_at = now()
  where id = p_order_id;

  update public.qurbani_shares
  set pool_id = v_target_pool_id, status = 'confirmed', confirmed_at = coalesce(confirmed_at, now()), updated_at = now()
  where order_id = p_order_id;

  if p_donation_id is not null then
    update public.donations set qurbani_order_id = p_order_id, updated_at = now() where id = p_donation_id;
  end if;

  if v_source_pool_id is not null and v_source_pool_id <> v_target_pool_id then
    perform private.qurbani_refresh_pool_counters(v_source_pool_id);
  end if;
  perform private.qurbani_refresh_pool_counters(v_target_pool_id);
  v_code := private.qurbani_finalize_pool_code(v_target_pool_id);

  insert into public.audit_logs(action, actor_email, target_collection, target_id, details, updated_at, created_at)
  values (
    'qurbani.order_payment_confirmed', nullif(p_actor, ''), 'qurbani-orders', p_order_id::text,
    jsonb_build_object('sourcePoolId', v_source_pool_id, 'poolId', v_target_pool_id, 'donationId', p_donation_id, 'providerId', p_provider_id),
    now(), now()
  );

  return query select v_target_pool_id, v_code, false;
end;
$$;

create or replace function private.qurbani_expire_reservations()
returns integer
language plpgsql
security invoker
set search_path = pg_catalog, public, private
as $$
declare
  v_order_ids integer[];
  v_pool_ids integer[];
  v_pool_id integer;
  v_count integer := 0;
begin
  with candidates as (
    select id, pool_id
    from public.qurbani_orders
    where status in ('reserved', 'pending_payment', 'pending_eft_review')
      and reserved_until <= now()
    order by reserved_until, id
    for update skip locked
    limit 500
  )
  select array_agg(id), array_agg(distinct pool_id)
    into v_order_ids, v_pool_ids
  from candidates;

  if v_order_ids is null then return 0; end if;
  v_count := cardinality(v_order_ids);

  update public.qurbani_orders
  set status = 'expired', failure_reason = coalesce(failure_reason, 'reservation_expired'), updated_at = now()
  where id = any(v_order_ids);

  update public.qurbani_shares
  set status = 'cancelled', updated_at = now()
  where order_id = any(v_order_ids) and status = 'reserved';

  foreach v_pool_id in array v_pool_ids loop
    if v_pool_id is not null then perform private.qurbani_refresh_pool_counters(v_pool_id); end if;
  end loop;

  insert into public.audit_logs(action, actor_email, target_collection, details, updated_at, created_at)
  values ('qurbani.reservations_expired', 'worker', 'qurbani-orders', jsonb_build_object('count', v_count, 'orderIds', v_order_ids), now(), now());
  return v_count;
end;
$$;

create or replace function private.qurbani_transfer_order(
  p_order_id integer,
  p_target_pool_id integer,
  p_actor_id integer
)
returns table (source_pool_id integer, target_pool_id integer, target_pool_code text)
language plpgsql
security invoker
set search_path = pg_catalog, public, private
as $$
declare
  v_order public.qurbani_orders%rowtype;
  v_source public.qurbani_pools%rowtype;
  v_target public.qurbani_pools%rowtype;
  v_actor_email text;
  v_code text;
  v_lock record;
begin
  select * into v_order from public.qurbani_orders where id = p_order_id for update;
  if not found or v_order.pool_id is null then raise exception 'Qurbani order or source pool not found'; end if;
  if v_order.pool_id = p_target_pool_id then raise exception 'Source and target pool must be different'; end if;
  if v_order.status not in ('reserved', 'pending_payment', 'pending_eft_review', 'paid') then
    raise exception 'Qurbani order % cannot be transferred from status %', p_order_id, v_order.status;
  end if;
  if v_order.status <> 'paid' and v_order.reserved_until <= now() then raise exception 'Reservation has expired'; end if;

  perform 1 from public.qurbani_products where id = v_order.product_id for update;
  for v_lock in
    select id from public.qurbani_pools where id in (v_order.pool_id, p_target_pool_id) order by id for update
  loop null; end loop;

  select * into v_source from public.qurbani_pools where id = v_order.pool_id;
  select * into v_target from public.qurbani_pools where id = p_target_pool_id;
  if not found then raise exception 'Target pool not found'; end if;
  if v_source.locked_at is not null or v_source.status in ('in_progress', 'video_processing', 'ready', 'notified', 'closed') then
    raise exception 'Source pool is locked because field work has started';
  end if;
  if v_target.locked_at is not null or v_target.status <> 'open' then raise exception 'Target pool must be open and unlocked'; end if;
  if v_target.product_id <> v_source.product_id or v_target.season_id <> v_source.season_id then
    raise exception 'Pools must belong to the same season and product';
  end if;

  perform private.qurbani_refresh_pool_counters(v_source.id);
  perform private.qurbani_refresh_pool_counters(v_target.id);
  select * into v_target from public.qurbani_pools where id = p_target_pool_id;
  if v_target.confirmed_count + v_target.reserved_count + v_order.share_count > v_target.capacity then
    raise exception 'Target pool does not have enough capacity for the complete order';
  end if;

  update public.qurbani_orders set pool_id = p_target_pool_id, updated_at = now() where id = p_order_id;
  update public.qurbani_shares set pool_id = p_target_pool_id, updated_at = now() where order_id = p_order_id;
  perform private.qurbani_refresh_pool_counters(v_source.id);
  perform private.qurbani_refresh_pool_counters(v_target.id);
  if v_order.status = 'paid' then v_code := private.qurbani_finalize_pool_code(v_target.id); end if;

  select email into v_actor_email from public.users where id = p_actor_id;
  insert into public.audit_logs(action, actor_email, target_collection, target_id, details, updated_at, created_at)
  values (
    'qurbani.order_transferred', v_actor_email, 'qurbani-orders', p_order_id::text,
    jsonb_build_object('sourcePoolId', v_source.id, 'targetPoolId', v_target.id, 'actorId', p_actor_id), now(), now()
  );

  return query select v_source.id, v_target.id, v_code;
end;
$$;

