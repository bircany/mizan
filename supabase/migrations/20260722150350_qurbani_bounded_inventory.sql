-- Legacy orders which were already reserved before this migration may still be
-- confirmed, but confirmation is constrained to their existing pool. It never
-- creates or silently changes inventory.
create or replace function private.qurbani_confirm_order_payment(
  p_order_id integer,
  p_donation_id integer default null,
  p_provider_id text default null,
  p_actor text default 'system'
)
returns table (pool_id integer, pool_code text, already_confirmed boolean)
language plpgsql
security invoker
set search_path=pg_catalog,public,private
as $$
declare
  v_order public.qurbani_orders%rowtype;
  v_pool public.qurbani_pools%rowtype;
begin
  select * into v_order from public.qurbani_orders where id=p_order_id for update;
  if not found then raise exception 'Qurbani order % not found',p_order_id; end if;
  if v_order.checkout_id is not null then
    raise exception 'CHECKOUT_ORDER_MUST_USE_QURBANI_FINALIZE_CHECKOUT';
  end if;
  if v_order.status='paid' then
    select * into v_pool from public.qurbani_pools where id=v_order.pool_id;
    return query select v_order.pool_id,v_pool.code,true;
    return;
  end if;
  if v_order.status in ('cancelled','refunded','failed','action_required') then
    raise exception 'Qurbani order % cannot be confirmed from status %',p_order_id,v_order.status;
  end if;
  if p_provider_id is not null and exists(
    select 1 from public.qurbani_orders where provider_payment_id=p_provider_id and id<>p_order_id
  ) then raise exception 'Provider payment id is already bound to another qurbani order'; end if;

  select * into v_pool from public.qurbani_pools where id=v_order.pool_id for update;
  if not found or v_pool.status='withdrawn' or v_pool.locked_at is not null then
    raise exception using errcode='P0001',message='OUT_OF_STOCK';
  end if;
  perform private.qurbani_refresh_pool_counters(v_pool.id);
  select * into v_pool from public.qurbani_pools where id=v_pool.id for update;
  if v_pool.confirmed_count + v_order.share_count > v_pool.capacity then
    update public.qurbani_orders set status='action_required',failure_reason='OUT_OF_STOCK',updated_at=now() where id=v_order.id;
    raise exception using errcode='P0001',message='OUT_OF_STOCK';
  end if;

  update public.qurbani_orders
  set donation_id=coalesce(p_donation_id,donation_id),
      provider_payment_id=coalesce(nullif(p_provider_id,''),provider_payment_id),
      provider_reference=coalesce(nullif(p_provider_id,''),provider_reference),
      status='paid',paid_at=coalesce(paid_at,now()),failure_reason=null,updated_at=now()
  where id=v_order.id;
  update public.qurbani_shares
  set status='confirmed',confirmed_at=coalesce(confirmed_at,now()),updated_at=now()
  where order_id=v_order.id;
  if p_donation_id is not null then
    update public.donations set qurbani_order_id=v_order.id,updated_at=now() where id=p_donation_id;
  end if;
  perform private.qurbani_refresh_pool_counters(v_pool.id);
  update public.qurbani_pools
  set status=case when confirmed_count=capacity then 'full'::public.enum_qurbani_pools_status else status end,
      full_at=case when confirmed_count=capacity then coalesce(full_at,now()) else full_at end,
      updated_at=now()
  where id=v_pool.id;
  insert into public.audit_logs(action,actor_email,target_collection,target_id,details,updated_at,created_at)
  values('qurbani.legacy_order_payment_confirmed',nullif(p_actor,''),'qurbani-orders',v_order.id::text,
    jsonb_build_object('poolId',v_pool.id,'donationId',p_donation_id,'providerId',p_provider_id),now(),now());
  select * into v_pool from public.qurbani_pools where id=v_pool.id;
  return query select v_pool.id,v_pool.code,false;
end;
$$;

