create or replace function private.qurbani_create_field_package(p_input jsonb)
returns jsonb
language plpgsql
security invoker
set search_path=pg_catalog,public,private
as $$
declare
  v_idempotency_key text:=btrim(coalesce(p_input->>'idempotencyKey',''));
  v_pool_ids integer[];
  v_existing public.qurbani_field_packages%rowtype;
  v_pool record;
  v_operator_id integer;
  v_package_id integer;
  v_package_code text;
  v_package_name text;
  v_season_id integer;
  v_country_id integer;
  v_region_id integer;
  v_year integer;
  v_country_code text;
  v_due_at timestamptz;
  v_pool_code text;
  v_task_id integer;
  v_task_ids integer[]:='{}';
  v_pool_codes text[]:='{}';
  v_seen integer:=0;
  v_ordinal integer:=0;
begin
  if v_idempotency_key='' or length(v_idempotency_key)>160 then
    raise exception 'A valid field package idempotency key is required';
  end if;
  if jsonb_typeof(p_input->'poolIds')<>'array'
    or jsonb_array_length(p_input->'poolIds')<1
    or jsonb_array_length(p_input->'poolIds')>250 then
    raise exception 'poolIds must contain 1-250 pools';
  end if;
  begin
    select array_agg(value::integer order by value::integer) into v_pool_ids
    from jsonb_array_elements_text(p_input->'poolIds');
    v_operator_id:=(p_input->>'operatorId')::integer;
  exception when invalid_text_representation or numeric_value_out_of_range then
    raise exception 'Pool and operator identifiers must be positive integers';
  end;
  if v_operator_id<1 or exists(select 1 from unnest(v_pool_ids) id where id<1) then
    raise exception 'Pool and operator identifiers must be positive integers';
  end if;
  if cardinality(v_pool_ids)<>(select count(distinct id) from unnest(v_pool_ids) id) then
    raise exception 'A pool can appear only once in a field package';
  end if;
  if not exists(select 1 from public.users where id=v_operator_id and role='field_operator') then
    raise exception 'The assignee must be an active field operator';
  end if;
  if coalesce((p_input->>'allowEarly')::boolean,false) then
    perform set_config('mizan.qurbani_allow_early','on',true);
  end if;

  -- Serializes duplicate submissions even before the unique package row exists.
  perform pg_advisory_xact_lock(hashtextextended(v_idempotency_key,7242026));
  select * into v_existing from public.qurbani_field_packages
  where idempotency_key=v_idempotency_key for update;
  if found then
    select coalesce(array_agg(item.field_task_id order by item.ordinal),'{}'),
           coalesce(array_agg(pool.code order by item.ordinal),'{}')
      into v_task_ids,v_pool_codes
    from public.qurbani_field_package_items item
    join public.qurbani_pools pool on pool.id=item.pool_id
    where item.field_package_id=v_existing.id;
    return jsonb_build_object(
      'packageId',v_existing.id,'code',v_existing.code,'fieldTaskIds',to_jsonb(v_task_ids),
      'poolCodes',to_jsonb(v_pool_codes),'idempotent',true
    );
  end if;

  -- All pool rows are locked in the same ascending order before any mutation.
  for v_pool in
    select pool.*,batch.region_id,product.campaign_id
    from public.qurbani_pools pool
    join public.qurbani_stock_batch_lines line on line.id=pool.stock_batch_line_id
    join public.qurbani_stock_batches batch on batch.id=line.batch_id
    join public.qurbani_products product on product.id=pool.product_id
    where pool.id=any(v_pool_ids)
    order by pool.id
    for update of pool
  loop
    v_seen:=v_seen+1;
    if v_pool.status<>'full' or v_pool.confirmed_count<>v_pool.capacity or v_pool.reserved_count<>0 then
      raise exception 'Pool % is not a fully paid field-ready pool',v_pool.id;
    end if;
    if v_pool.locked_at is not null or v_pool.field_task_id is not null
      or exists(select 1 from public.qurbani_field_package_items item where item.pool_id=v_pool.id)
      or exists(select 1 from public.field_tasks task where task.qurbani_pool_id=v_pool.id) then
      raise exception 'Pool % is already locked or packaged',v_pool.id;
    end if;
    if v_pool.country_id is null or v_pool.region_id is null then
      raise exception 'Pool % has no season-country operation configuration',v_pool.id;
    end if;
    if v_seen=1 then
      v_season_id:=v_pool.season_id;
      v_country_id:=v_pool.country_id;
      v_region_id:=v_pool.region_id;
    elsif v_pool.season_id<>v_season_id or v_pool.country_id<>v_country_id then
      raise exception 'All field package pools must belong to the same season and country';
    end if;
  end loop;
  if v_seen<>cardinality(v_pool_ids) then raise exception 'One or more pools were not found in bounded stock'; end if;

  select season.year::integer,country.iso_code,coalesce(nullif(p_input->>'dueAt','')::timestamptz,season.eid_at)
    into v_year,v_country_code,v_due_at
  from public.qurbani_seasons season cross join public.qurbani_countries country
  where season.id=v_season_id and country.id=v_country_id;
  if not found then raise exception 'Field package season or country is missing'; end if;
  v_package_code:=format('SP-%s-%s-%s',v_year,v_country_code,lpad(nextval('private.qurbani_field_package_code_seq')::text,6,'0'));
  v_package_name:=coalesce(nullif(btrim(p_input->>'name'),''),format('%s %s saha paketi',v_year,v_country_code));

  insert into public.qurbani_field_packages(
    season_id,country_id,region_id,code,name,idempotency_key,status,assigned_to_id,due_at,notes,updated_at,created_at
  ) values(
    v_season_id,v_country_id,v_region_id,v_package_code,v_package_name,v_idempotency_key,'assigned',v_operator_id,
    v_due_at,nullif(p_input->>'notes',''),now(),now()
  ) returning id into v_package_id;

  for v_pool in
    select pool.id,pool.product_id,product.campaign_id
    from public.qurbani_pools pool
    join public.qurbani_products product on product.id=pool.product_id
    where pool.id=any(v_pool_ids)
    order by pool.id
  loop
    v_ordinal:=v_ordinal+1;
    v_pool_code:=private.qurbani_finalize_pool_code(v_pool.id);
    if v_pool_code is null then raise exception 'Pool % could not receive a field code',v_pool.id; end if;
    insert into public.field_tasks(
      campaign_id,qurbani_pool_id,title,location,assigned_to_id,due_at,status,notes,updated_at,created_at
    ) values(
      v_pool.campaign_id,v_pool.id,format('Kurban %s saha görevi',v_pool_code),v_country_code,v_operator_id,
      v_due_at,'todo',nullif(p_input->>'notes',''),now(),now()
    ) returning id into v_task_id;
    insert into public.qurbani_field_package_items(
      field_package_id,pool_id,ordinal,status,field_task_id,updated_at,created_at
    ) values(v_package_id,v_pool.id,v_ordinal,'planned',v_task_id,now(),now());
    update public.qurbani_pools
    set field_task_id=v_task_id,status='assigned',locked_at=now(),updated_at=now()
    where id=v_pool.id;
    v_task_ids:=array_append(v_task_ids,v_task_id);
    v_pool_codes:=array_append(v_pool_codes,v_pool_code);
  end loop;

  insert into public.audit_logs(action,actor_email,target_collection,target_id,details,updated_at,created_at)
  values(
    'qurbani.field_package_created',nullif(p_input->>'actorEmail',''),'qurbani-field-packages',v_package_id::text,
    jsonb_build_object('operatorId',v_operator_id,'poolIds',to_jsonb(v_pool_ids),'fieldTaskIds',to_jsonb(v_task_ids),'actorId',p_input->>'actorId'),
    now(),now()
  );
  return jsonb_build_object(
    'packageId',v_package_id,'code',v_package_code,'fieldTaskIds',to_jsonb(v_task_ids),
    'poolCodes',to_jsonb(v_pool_codes),'idempotent',false
  );
end;
$$;

create or replace function private.qurbani_finalize_incomplete_pool(p_input jsonb)
returns jsonb
language plpgsql security invoker
set search_path=pg_catalog,public,private
as $$
declare v_pool public.qurbani_pools%rowtype; v_new_capacity integer;
begin
  select * into v_pool from public.qurbani_pools where id=(p_input->>'poolId')::integer for update;
  if not found then raise exception 'Pool not found'; end if;
  perform private.qurbani_refresh_pool_counters(v_pool.id);
  select * into v_pool from public.qurbani_pools where id=v_pool.id for update;
  if v_pool.locked_at is not null or v_pool.field_task_id is not null or v_pool.code is not null
    or v_pool.status not in ('open','full') then raise exception 'Pool can no longer be resized'; end if;
  if v_pool.reserved_count<>0 or v_pool.confirmed_count<1 or v_pool.confirmed_count>=v_pool.capacity then
    raise exception 'Only a partially paid pool without active holds can be finalized';
  end if;
  if exists(select 1 from public.qurbani_inventory_holds where pool_id=v_pool.id and status='active') then
    raise exception 'Pool has active checkout holds';
  end if;
  v_new_capacity:=v_pool.confirmed_count::integer;
  update public.qurbani_pools set capacity=v_new_capacity,status='full',full_at=coalesce(full_at,now()),
    operation_notes=concat_ws(E'\n',nullif(operation_notes,''),nullif(p_input->>'reason','')),updated_at=now()
  where id=v_pool.id;
  insert into public.audit_logs(action,actor_email,target_collection,target_id,details,updated_at,created_at)
  values('qurbani.incomplete_pool_finalized',nullif(p_input->>'actorEmail',''),'qurbani-pools',v_pool.id::text,
    jsonb_build_object('oldCapacity',v_pool.capacity,'newCapacity',v_new_capacity,'reason',p_input->>'reason','actorId',p_input->>'actorId'),now(),now());
  return jsonb_build_object('poolId',v_pool.id,'oldCapacity',v_pool.capacity,'newCapacity',v_new_capacity);
end;
$$;

