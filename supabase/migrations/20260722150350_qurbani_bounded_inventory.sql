create or replace function private.qurbani_product_availability(p_product_id integer)
returns jsonb
language plpgsql security invoker
set search_path=pg_catalog,public,private
as $$
declare
  v_product public.qurbani_products%rowtype;
  v_price public.qurbani_price_revisions%rowtype;
  v_remaining integer := 0;
begin
  select * into v_product from public.qurbani_products where id=p_product_id and is_active;
  if not found or v_product.current_price_revision_id is null then
    return jsonb_build_object('productId',p_product_id,'remainingShares',0,'code','NOT_CONFIGURED');
  end if;
  select * into v_price from public.qurbani_price_revisions
  where id=v_product.current_price_revision_id and status='active'
    and effective_from<=now() and (effective_to is null or effective_to>now());
  if not found then return jsonb_build_object('productId',p_product_id,'remainingShares',0,'code','PRICE_UNAVAILABLE'); end if;

  select coalesce(sum(greatest(pool.capacity-pool.confirmed_count-pool.reserved_count,0)),0)::integer
    into v_remaining
  from public.qurbani_pools pool
  join public.qurbani_stock_batch_lines line on line.id=pool.stock_batch_line_id and line.status='active'
  join public.qurbani_stock_batches batch on batch.id=line.batch_id and batch.status='active'
  join public.qurbani_regions region on region.id=batch.region_id and region.is_active
  where pool.product_id=p_product_id and pool.status='open' and pool.locked_at is null
    and (pool.price_revision_id=v_price.id or (pool.confirmed_count=0 and pool.reserved_count=0))
    and now() between coalesce(line.sales_start_at,batch.sales_start_at,region.sales_start_at)
                  and coalesce(line.sales_end_at,batch.sales_end_at,region.sales_end_at);

  return jsonb_build_object(
    'productId',p_product_id,'stockBatchLineId',v_price.batch_line_id,'priceRevisionId',v_price.id,
    'revision',v_price.revision,'unitPrice',v_price.unit_price,'currency',v_price.currency,
    'remainingShares',v_remaining
  );
end;
$$;

create or replace function private.qurbani_create_checkout(p_input jsonb)
returns jsonb
language plpgsql security invoker
set search_path=pg_catalog,public,private
as $$
declare
  v_items jsonb := p_input->'items';
  v_envelope jsonb := p_input->'piiEnvelope';
  v_item jsonb;
  v_product public.qurbani_products%rowtype;
  v_price public.qurbani_price_revisions%rowtype;
  v_pool record;
  v_pool_ids integer[];
  v_hold_quantities integer[];
  v_checkout_id integer;
  v_checkout_line_id integer;
  v_campaign_id integer;
  v_funding_pool_id integer;
  v_currency public.enum_qurbani_products_currency;
  v_quantity integer;
  v_total numeric := 0;
  v_expires_at timestamptz := now()+interval '30 minutes';
  v_public_id text;
  v_index integer := 0;
  v_remaining_quantity integer;
  v_take integer;
  v_position integer;
begin
  if jsonb_typeof(v_items)<>'array' or jsonb_array_length(v_items)<1 or jsonb_array_length(v_items)>10 then
    raise exception 'items must contain 1-10 product lines';
  end if;
  if jsonb_typeof(v_envelope)<>'object'
    or coalesce(nullif(v_envelope->>'ciphertext',''),nullif(v_envelope->>'data','')) is null
    or nullif(v_envelope->>'iv','') is null or nullif(v_envelope->>'tag','') is null
    or coalesce(nullif(v_envelope->>'keyVersion',''),nullif(v_envelope->>'kid','')) is null then
    raise exception 'A complete encrypted PII envelope is required';
  end if;
  if (select count(*) from jsonb_array_elements(v_items)) <>
     (select count(distinct (value->>'productId')::integer) from jsonb_array_elements(v_items)) then
    raise exception 'A product can appear only once per checkout';
  end if;

  begin
    for v_item in select value from jsonb_array_elements(v_items) loop
      v_quantity := (v_item->>'quantity')::integer;
      if v_quantity<1 or v_quantity>100 then raise exception 'Invalid quantity'; end if;

      select * into v_product from public.qurbani_products
      where id=(v_item->>'productId')::integer and is_active for update;
      if not found then raise exception using errcode='Q0001',message='OUT_OF_STOCK'; end if;
      if v_index=0 then
        v_campaign_id:=v_product.campaign_id; v_funding_pool_id:=v_product.funding_pool_id; v_currency:=v_product.currency;
        v_public_id:=format('QC-%s-%s',(select year::integer from public.qurbani_seasons where id=v_product.season_id),lpad(nextval('private.qurbani_checkout_public_seq')::text,10,'0'));
        insert into public.qurbani_checkouts(
          public_id,buyer_mask,buyer_hash,encrypted_payload,encryption_iv,encryption_tag,encryption_key_version,
          campaign_id,funding_pool_id,total_amount,currency,status,source,expires_at,ip_address,pii_purge_at,updated_at,created_at
        ) values(
          v_public_id,nullif(p_input->>'buyerMask',''),nullif(p_input->>'buyerHash',''),
          coalesce(v_envelope->>'ciphertext',v_envelope->>'data'),v_envelope->>'iv',v_envelope->>'tag',coalesce(v_envelope->>'keyVersion',v_envelope->>'kid'),
          v_campaign_id,v_funding_pool_id,0,v_currency,'held','website',v_expires_at,nullif(p_input->>'ipAddress',''),now()+interval '24 hours',now(),now()
        ) returning id into v_checkout_id;
      elsif v_product.currency<>v_currency then
        raise exception 'All checkout items must use the same currency';
      end if;

      select * into v_price from public.qurbani_price_revisions
      where id=coalesce((v_item->>'priceRevisionId')::integer,v_product.current_price_revision_id)
        and product_id=v_product.id and status='active' and effective_from<=now()
        and (effective_to is null or effective_to>now()) for update;
      if not found or v_price.id<>v_product.current_price_revision_id then
        raise exception using errcode='Q0002',message='PRICE_CHANGED';
      end if;

      if v_product.kind='cattle' then
        v_pool_ids:='{}'; v_hold_quantities:='{}'; v_remaining_quantity:=v_quantity;
        select pool.id,pool.stock_batch_line_id,(pool.capacity-pool.confirmed_count-pool.reserved_count)::integer available into v_pool
        from public.qurbani_pools pool
        join public.qurbani_stock_batch_lines line on line.id=pool.stock_batch_line_id and line.status='active'
        join public.qurbani_stock_batches batch on batch.id=line.batch_id and batch.status='active'
        join public.qurbani_regions region on region.id=batch.region_id and region.is_active
        where pool.product_id=v_product.id and pool.status='open' and pool.locked_at is null
          and pool.capacity-pool.confirmed_count-pool.reserved_count>=v_quantity
          and (pool.price_revision_id=v_price.id or (pool.confirmed_count=0 and pool.reserved_count=0))
          and now() between coalesce(line.sales_start_at,batch.sales_start_at,region.sales_start_at)
                        and coalesce(line.sales_end_at,batch.sales_end_at,region.sales_end_at)
        order by ((pool.confirmed_count+pool.reserved_count)>0) desc,pool.id for update of pool skip locked limit 1;
        if found then
          v_pool_ids:=array_append(v_pool_ids,v_pool.id); v_hold_quantities:=array_append(v_hold_quantities,v_quantity); v_remaining_quantity:=0;
        else
          for v_pool in
            select pool.id,pool.stock_batch_line_id,(pool.capacity-pool.confirmed_count-pool.reserved_count)::integer available
            from public.qurbani_pools pool
            join public.qurbani_stock_batch_lines line on line.id=pool.stock_batch_line_id and line.status='active'
            join public.qurbani_stock_batches batch on batch.id=line.batch_id and batch.status='active'
            join public.qurbani_regions region on region.id=batch.region_id and region.is_active
            where pool.product_id=v_product.id and pool.status='open' and pool.locked_at is null
              and pool.capacity-pool.confirmed_count-pool.reserved_count>0
              and (pool.price_revision_id=v_price.id or (pool.confirmed_count=0 and pool.reserved_count=0))
              and now() between coalesce(line.sales_start_at,batch.sales_start_at,region.sales_start_at)
                            and coalesce(line.sales_end_at,batch.sales_end_at,region.sales_end_at)
            order by (pool.capacity-pool.confirmed_count-pool.reserved_count) desc,pool.id
            for update of pool skip locked
          loop
            v_take:=least(v_remaining_quantity,v_pool.available);
            v_pool_ids:=array_append(v_pool_ids,v_pool.id); v_hold_quantities:=array_append(v_hold_quantities,v_take);
            v_remaining_quantity:=v_remaining_quantity-v_take; exit when v_remaining_quantity=0;
          end loop;
        end if;
        if v_remaining_quantity<>0 then raise exception using errcode='Q0001',message='OUT_OF_STOCK'; end if;
        select stock_batch_line_id into v_pool from public.qurbani_pools where id=v_pool_ids[1];
        insert into public.qurbani_checkout_lines(
          checkout_id,product_id,stock_batch_line_id,price_revision_id,quantity,participant_count,encrypted_item_index,
          unit_price,total_amount,currency,status,updated_at,created_at
        ) values(v_checkout_id,v_product.id,v_pool.stock_batch_line_id,v_price.id,v_quantity,v_quantity,v_index,v_price.unit_price,v_price.unit_price*v_quantity,v_price.currency,'held',now(),now())
        returning id into v_checkout_line_id;
        for v_position in 1..cardinality(v_pool_ids) loop
          update public.qurbani_pools set price_revision_id=v_price.id,reserved_count=reserved_count+v_hold_quantities[v_position],updated_at=now() where id=v_pool_ids[v_position];
          insert into public.qurbani_inventory_holds(checkout_id,checkout_line_id,pool_id,price_revision_id,quantity,status,expires_at,updated_at,created_at)
          values(v_checkout_id,v_checkout_line_id,v_pool_ids[v_position],v_price.id,v_hold_quantities[v_position],'active',v_expires_at,now(),now());
        end loop;
      else
        select array_agg(candidate.id order by candidate.id) into v_pool_ids
        from (
          select pool.id
          from public.qurbani_pools pool
          join public.qurbani_stock_batch_lines line on line.id=pool.stock_batch_line_id and line.status='active'
          join public.qurbani_stock_batches batch on batch.id=line.batch_id and batch.status='active'
          join public.qurbani_regions region on region.id=batch.region_id and region.is_active
          where pool.product_id=v_product.id and pool.status='open' and pool.locked_at is null
            and pool.capacity=1 and pool.confirmed_count=0 and pool.reserved_count=0
            and (pool.price_revision_id=v_price.id or (pool.confirmed_count=0 and pool.reserved_count=0))
            and now() between coalesce(line.sales_start_at,batch.sales_start_at,region.sales_start_at)
                          and coalesce(line.sales_end_at,batch.sales_end_at,region.sales_end_at)
          order by pool.id for update of pool skip locked limit v_quantity
        ) candidate;
        if coalesce(cardinality(v_pool_ids),0)<>v_quantity then raise exception using errcode='Q0001',message='OUT_OF_STOCK'; end if;
        select stock_batch_line_id into v_pool from public.qurbani_pools where id=v_pool_ids[1];
        update public.qurbani_pools set price_revision_id=v_price.id,reserved_count=1,updated_at=now() where id=any(v_pool_ids);
        insert into public.qurbani_checkout_lines(
          checkout_id,product_id,stock_batch_line_id,price_revision_id,quantity,participant_count,encrypted_item_index,
          unit_price,total_amount,currency,status,updated_at,created_at
        ) values(v_checkout_id,v_product.id,v_pool.stock_batch_line_id,v_price.id,v_quantity,v_quantity,v_index,v_price.unit_price,v_price.unit_price*v_quantity,v_price.currency,'held',now(),now())
        returning id into v_checkout_line_id;
        insert into public.qurbani_inventory_holds(checkout_id,checkout_line_id,pool_id,price_revision_id,quantity,status,expires_at,updated_at,created_at)
        select v_checkout_id,v_checkout_line_id,pool_id,v_price.id,1,'active',v_expires_at,now(),now() from unnest(v_pool_ids) pool_id;
      end if;
      v_total:=v_total+v_price.unit_price*v_quantity;
      v_index:=v_index+1;
    end loop;

    update public.qurbani_checkouts set total_amount=v_total,updated_at=now() where id=v_checkout_id;
    return jsonb_build_object('success',true,'checkoutId',v_checkout_id,'publicId',v_public_id,'totalAmount',v_total,'currency',v_currency,'expiresAt',v_expires_at);
  exception
    when sqlstate 'Q0001' then return jsonb_build_object('success',false,'code','OUT_OF_STOCK');
    when sqlstate 'Q0002' then return jsonb_build_object('success',false,'code','PRICE_CHANGED');
  end;
end;
$$;

create or replace function private.qurbani_finalize_pool_code(p_pool_id integer)
returns character varying
language plpgsql security invoker
set search_path=pg_catalog,public,private
as $$
declare
  v_region_id integer; v_country_id integer; v_year integer; v_counter integer;
  v_capacity integer; v_confirmed integer; v_code text; v_field_preparation_at timestamptz;
begin
  select batch.region_id,batch.country_id,season.year::integer,pool.capacity::integer,pool.confirmed_count::integer,pool.code,region.field_preparation_at
  into v_region_id,v_country_id,v_year,v_capacity,v_confirmed,v_code,v_field_preparation_at
  from public.qurbani_pools pool
  join public.qurbani_stock_batch_lines line on line.id=pool.stock_batch_line_id
  join public.qurbani_stock_batches batch on batch.id=line.batch_id
  join public.qurbani_seasons season on season.id=pool.season_id
  left join public.qurbani_regions region on region.id=batch.region_id
  where pool.id=p_pool_id for update of pool;
  if not found then raise exception 'Bounded qurbani pool % not found',p_pool_id; end if;
  if v_confirmed<>v_capacity or v_code is not null then return v_code; end if;
  if v_region_id is null then raise exception 'Season-country operation configuration is missing'; end if;
  if (v_field_preparation_at is null or now()<v_field_preparation_at)
    and coalesce(current_setting('mizan.qurbani_allow_early',true),'off')<>'on' then
    raise exception 'Qurbani code can only be generated during field preparation';
  end if;

  update public.qurbani_regions set code_counter=code_counter+1,updated_at=now()
  where id=v_region_id and country_id=v_country_id
  returning code_counter::integer into v_counter;
  if not found or v_counter>9999 then raise exception 'Country qurbani code capacity exhausted'; end if;
  v_code:=format('MD-%s-%s',v_year,lpad(v_counter::text,4,'0'));
  update public.qurbani_pools set code=v_code,status='full',full_at=coalesce(full_at,now()),updated_at=now() where id=p_pool_id;
  return v_code;
end;
$$;

create or replace function private.qurbani_finalize_checkout(
  p_checkout_id integer,
  p_payment_id text,
  p_donation_id integer,
  p_actor text,
  p_decrypted_payload jsonb
)
returns jsonb
language plpgsql security invoker
set search_path=pg_catalog,public,private
as $$
declare
  v_checkout public.qurbani_checkouts%rowtype;
  v_line public.qurbani_checkout_lines%rowtype;
  v_hold public.qurbani_inventory_holds%rowtype;
  v_product public.qurbani_products%rowtype;
  v_buyer jsonb:=p_decrypted_payload->'buyer';
  v_consents jsonb:=p_decrypted_payload->'consents';
  v_group jsonb; v_people jsonb; v_person jsonb;
  v_order_id integer; v_order_ids integer[]:='{}'; v_first_pool integer;
  v_offset integer; v_i integer; v_order_number text; v_accepted_at timestamptz;
  v_identity_type public.enum_qurbani_orders_identity_type;
begin
  select * into v_checkout from public.qurbani_checkouts where id=p_checkout_id for update;
  if not found then raise exception 'Checkout not found'; end if;
  if v_checkout.status='succeeded' then
    select coalesce(array_agg(id order by id),'{}') into v_order_ids from public.qurbani_orders where checkout_id=p_checkout_id;
    return jsonb_build_object('state','already_finalized','checkoutId',p_checkout_id,'orderIds',to_jsonb(v_order_ids));
  end if;
  if v_checkout.status not in ('held','payment_initialized','payment_received_processing') then raise exception 'Checkout status cannot be finalized'; end if;
  if nullif(p_payment_id,'') is null then raise exception 'Verified provider payment id is required'; end if;
  if exists(select 1 from public.qurbani_checkouts where provider_payment_id=p_payment_id and id<>p_checkout_id) then raise exception 'Provider payment already used'; end if;

  update public.qurbani_checkouts set status='payment_received_processing',provider_payment_id=p_payment_id,
    donation_id=coalesce(p_donation_id,donation_id),failure_code=null,updated_at=now() where id=p_checkout_id;

  begin
    if jsonb_typeof(v_buyer)<>'object' or jsonb_typeof(p_decrypted_payload->'shareholdersByProduct')<>'array' then raise exception 'Decrypted checkout payload is invalid'; end if;
    if not coalesce((v_consents->>'digitalPowerOfAttorney')::boolean,false)
      or not coalesce((v_consents->>'kvkk')::boolean,false)
      or not coalesce((v_consents->>'terms')::boolean,false) then raise exception 'Required consent is missing'; end if;
    v_accepted_at:=coalesce(nullif(v_consents->>'acceptedAt','')::timestamptz,now());
    v_identity_type:=case when upper(coalesce(v_buyer->>'countryCode','TR'))='TR' then 'tc_identity'::public.enum_qurbani_orders_identity_type else 'passport'::public.enum_qurbani_orders_identity_type end;

    for v_line in select * from public.qurbani_checkout_lines where checkout_id=p_checkout_id order by id for update loop
      select * into v_product from public.qurbani_products where id=v_line.product_id;
      select value into v_group from jsonb_array_elements(p_decrypted_payload->'shareholdersByProduct')
        where (value->>'productId')::integer=v_line.product_id limit 1;
      v_people:=v_group->'shareholders';
      if jsonb_typeof(v_people)<>'array' or jsonb_array_length(v_people)<>v_line.quantity then raise exception 'Shareholder count does not match product quantity'; end if;
      if exists(select 1 from jsonb_array_elements(v_people) person where btrim(coalesce(person->>'name',''))='') then raise exception 'Every official share owner needs a name'; end if;
      select pool_id into v_first_pool from public.qurbani_inventory_holds where checkout_line_id=v_line.id and status='active' order by id limit 1;
      if v_first_pool is null then raise exception 'Active inventory hold is missing'; end if;

      v_order_number:=format('KB-%s-%s',(select year::integer from public.qurbani_seasons where id=v_product.season_id),lpad(nextval('private.qurbani_order_number_seq')::text,8,'0'));
      insert into public.qurbani_orders(
        order_number,season_id,product_id,pool_id,checkout_id,checkout_line_id,price_revision_id,donation_id,
        buyer_name,buyer_email,buyer_phone,country_code,identity_type,identity_number,address_snapshot,product_snapshot,
        unit_price,share_count,total_amount,currency,payment_method,reservation_kind,status,reserved_until,
        provider_payment_id,provider_reference,proxy_text_version,proxy_accepted_at,proxy_accepted_ip,
        phone_proxy_confirmed_at,kvkk_accepted_at,terms_accepted_at,third_party_contact_consent_at,paid_at,is_legacy,updated_at,created_at
      ) values(
        v_order_number,v_product.season_id,v_product.id,v_first_pool,p_checkout_id,v_line.id,v_line.price_revision_id,p_donation_id,
        btrim(concat_ws(' ',v_buyer->>'firstName',v_buyer->>'lastName')),
        coalesce(nullif(v_buyer->>'email',''),format('checkout-%s@mizan.invalid',p_checkout_id)),
        v_buyer->>'phone',upper(coalesce(nullif(v_buyer->>'countryCode',''),'TR')),v_identity_type,nullif(v_buyer->>'identityNumber',''),
        jsonb_build_object('city',v_buyer->>'city','address',v_buyer->>'address','countryCode',v_buyer->>'countryCode'),
        jsonb_build_object('productId',v_product.id,'priceRevisionId',v_line.price_revision_id,'unitPrice',v_line.unit_price,'currency',v_line.currency,'capacity',v_product.capacity),
        v_line.unit_price,v_line.quantity,v_line.total_amount,v_line.currency::text::public.enum_qurbani_orders_currency,
        case when v_checkout.source='manual' then 'eft'::public.enum_qurbani_orders_payment_method else 'iyzico'::public.enum_qurbani_orders_payment_method end,
        case when v_checkout.source='manual' then 'eft'::public.enum_qurbani_orders_reservation_kind else 'iyzico'::public.enum_qurbani_orders_reservation_kind end,
        'paid',v_checkout.expires_at,p_payment_id,v_checkout.public_id,
        (select proxy_text_version from public.qurbani_seasons where id=v_product.season_id),v_accepted_at,coalesce(v_checkout.ip_address,'system'),
        case when v_checkout.source='manual' then v_accepted_at else null end,v_accepted_at,v_accepted_at,
        case when coalesce((v_consents->>'thirdPartyContact')::boolean,false) then v_accepted_at else null end,
        now(),false,now(),now()
      ) returning id into v_order_id;
      v_order_ids:=array_append(v_order_ids,v_order_id); v_offset:=0;

      for v_hold in select * from public.qurbani_inventory_holds where checkout_line_id=v_line.id and status='active' order by id for update loop
        insert into public.qurbani_allocations(checkout_id,checkout_line_id,order_id,pool_id,hold_id,price_revision_id,quantity,status,provider_payment_id,idempotency_key,updated_at,created_at)
        values(p_checkout_id,v_line.id,v_order_id,v_hold.pool_id,v_hold.id,v_hold.price_revision_id,v_hold.quantity,'confirmed',p_payment_id,format('checkout:%s:hold:%s',p_checkout_id,v_hold.id),now(),now());
        for v_i in 0..v_hold.quantity::integer-1 loop
          v_person:=v_people->(v_offset+v_i);
          insert into public.qurbani_shares(order_id,pool_id,owner_name,owner_phone,effective_phone,sequence,status,confirmed_at,updated_at,created_at)
          values(v_order_id,v_hold.pool_id,btrim(v_person->>'name'),nullif(btrim(v_person->>'phone'),''),coalesce(nullif(btrim(v_person->>'phone'),''),v_buyer->>'phone'),v_offset+v_i+1,'confirmed',now(),now(),now());
        end loop;
        v_offset:=v_offset+v_hold.quantity::integer;
        update public.qurbani_inventory_holds set status='consumed',consumed_at=now(),updated_at=now() where id=v_hold.id;
        update public.qurbani_pools
        set reserved_count=reserved_count-v_hold.quantity,
            confirmed_count=confirmed_count+v_hold.quantity,
            status=case when confirmed_count+v_hold.quantity=capacity then 'full'::public.enum_qurbani_pools_status else status end,
            full_at=case when confirmed_count+v_hold.quantity=capacity then coalesce(full_at,now()) else full_at end,
            updated_at=now()
        where id=v_hold.pool_id;
      end loop;
      update public.qurbani_checkout_lines set status='finalized',order_id=v_order_id,updated_at=now() where id=v_line.id;
    end loop;

    update public.qurbani_checkouts set status='succeeded',finalized_at=now(),failure_code=null,updated_at=now() where id=p_checkout_id;
    if p_donation_id is not null then update public.donations set qurbani_checkout_id=p_checkout_id,qurbani_order_id=v_order_ids[1],updated_at=now() where id=p_donation_id; end if;
    insert into public.audit_logs(action,actor_email,target_collection,target_id,details,updated_at,created_at)
      values('qurbani.checkout_finalized',nullif(p_actor,''),'qurbani-checkouts',p_checkout_id::text,jsonb_build_object('paymentId',p_payment_id,'orderIds',v_order_ids),now(),now());
    return jsonb_build_object('state','finalized','checkoutId',p_checkout_id,'orderIds',to_jsonb(v_order_ids));
  exception when others then
    update public.qurbani_checkouts set status='payment_received_processing',failure_code='FINALIZE_'||sqlstate,updated_at=now() where id=p_checkout_id;
    return jsonb_build_object('state','processing','checkoutId',p_checkout_id,'code','FINALIZE_'||sqlstate);
  end;
end;
$$;

create or replace function private.qurbani_fail_checkout(p_checkout_id integer,p_failure_code text)
returns jsonb
language plpgsql security invoker
set search_path=pg_catalog,public,private
as $$
declare v_checkout public.qurbani_checkouts%rowtype; v_hold public.qurbani_inventory_holds%rowtype;
begin
  select * into v_checkout from public.qurbani_checkouts where id=p_checkout_id for update;
  if not found then return jsonb_build_object('success',false,'code','NOT_FOUND'); end if;
  if v_checkout.status in ('succeeded','payment_received_processing') then return jsonb_build_object('success',false,'code','PAYMENT_ALREADY_RECEIVED'); end if;
  if v_checkout.status in ('failed','expired','cancelled') then return jsonb_build_object('success',true,'state',v_checkout.status); end if;
  for v_hold in select * from public.qurbani_inventory_holds where checkout_id=p_checkout_id and status='active' order by pool_id for update loop
    update public.qurbani_pools set reserved_count=greatest(reserved_count-v_hold.quantity,0),updated_at=now() where id=v_hold.pool_id;
    update public.qurbani_inventory_holds set status='released',released_at=now(),updated_at=now() where id=v_hold.id;
  end loop;
  update public.qurbani_checkout_lines set status='released',updated_at=now() where checkout_id=p_checkout_id and status='held';
  update public.qurbani_checkouts set status='failed',failure_code=left(coalesce(nullif(p_failure_code,''),'PAYMENT_INITIALIZATION_FAILED'),120),updated_at=now() where id=p_checkout_id;
  return jsonb_build_object('success',true,'state','failed','checkoutId',p_checkout_id);
end;
$$;

create or replace function private.qurbani_expire_holds()
returns integer
language plpgsql security invoker
set search_path=pg_catalog,public,private
as $$
declare v_checkout_id integer; v_hold public.qurbani_inventory_holds%rowtype; v_count integer:=0;
begin
  for v_checkout_id in
    select id from public.qurbani_checkouts
    where status in ('held','payment_initialized') and expires_at<=now()
    order by expires_at,id for update skip locked limit 500
  loop
    for v_hold in select * from public.qurbani_inventory_holds where checkout_id=v_checkout_id and status='active' order by pool_id for update loop
      update public.qurbani_pools set reserved_count=greatest(reserved_count-v_hold.quantity,0),updated_at=now() where id=v_hold.pool_id;
      update public.qurbani_inventory_holds set status='expired',released_at=now(),updated_at=now() where id=v_hold.id;
    end loop;
    update public.qurbani_checkout_lines set status='expired',updated_at=now() where checkout_id=v_checkout_id and status='held';
    update public.qurbani_checkouts set status='expired',failure_code='HOLD_EXPIRED',updated_at=now() where id=v_checkout_id;
    v_count:=v_count+1;
  end loop;
  return v_count;
end;
$$;

create or replace function private.qurbani_purge_checkout_pii(p_limit integer default 500)
returns integer
language plpgsql security invoker
set search_path=pg_catalog,public,private
as $$
declare v_count integer;
begin
  with candidates as (
    select id from public.qurbani_checkouts where pii_purged_at is null and pii_purge_at<=now()
    order by pii_purge_at,id for update skip locked limit greatest(1,least(coalesce(p_limit,500),2000))
  ), updated as (
    update public.qurbani_checkouts checkout set encrypted_payload=null,encryption_iv=null,encryption_tag=null,
      encryption_key_version=null,pii_purged_at=now(),updated_at=now()
    from candidates where checkout.id=candidates.id returning checkout.id
  ) select count(*) into v_count from updated;
  return v_count;
end;
$$;

create or replace function private.qurbani_create_stock_batch(p_input jsonb)
returns jsonb
language plpgsql security invoker
set search_path=pg_catalog,public,private
as $$
declare
  v_existing public.qurbani_stock_batches%rowtype; v_batch_id integer; v_region_id integer;
  v_row jsonb; v_product public.qurbani_products%rowtype; v_line_id integer; v_revision_id integer; v_revision integer;
  v_animal_count integer:=0; v_total_capacity integer:=0; v_i integer; v_status public.enum_qurbani_stock_batches_status;
  v_nature public.enum_qurbani_stock_batches_nature; v_line_status public.enum_qurbani_stock_batch_lines_status;
  v_code text; v_year integer; v_country_code text;
begin
  select * into v_existing from public.qurbani_stock_batches where idempotency_key=p_input->>'idempotencyKey' for update;
  if found then return jsonb_build_object('batchId',v_existing.id,'animalCount',v_existing.animal_count,'totalCapacity',v_existing.total_capacity,'availableCapacity',v_existing.available_capacity,'idempotent',true); end if;
  if jsonb_typeof(p_input->'rows')<>'array' or jsonb_array_length(p_input->'rows')<1 or jsonb_array_length(p_input->'rows')>50 then raise exception 'Batch rows must contain 1-50 entries'; end if;
  if (select count(*) from jsonb_array_elements(p_input->'rows')) <>
     (select count(distinct (value->>'productId')::integer) from jsonb_array_elements(p_input->'rows')) then
    raise exception 'A product can appear only once in a stock batch';
  end if;
  v_status:=(p_input->>'status')::public.enum_qurbani_stock_batches_status;
  v_nature:=(p_input->>'nature')::public.enum_qurbani_stock_batches_nature;
  select r.id into v_region_id from public.qurbani_regions r
    where r.id=coalesce((p_input->>'regionId')::integer,r.id)
      and r.season_id=(p_input->>'seasonId')::integer and r.country_id=(p_input->>'countryId')::integer
    order by r.is_active desc,r.id limit 1 for update;
  if v_region_id is null then raise exception 'Season-country operation configuration is required'; end if;
  select s.year::integer,c.iso_code into v_year,v_country_code
    from public.qurbani_seasons s cross join public.qurbani_countries c
    where s.id=(p_input->>'seasonId')::integer and c.id=(p_input->>'countryId')::integer;
  if not found then raise exception 'Season or country not found'; end if;
  v_code:=format('STK-%s-%s-%s',v_year,v_country_code,lpad(nextval('private.qurbani_stock_batch_code_seq')::text,6,'0'));
  insert into public.qurbani_stock_batches(
    season_id,country_id,region_id,code,name,nature,status,idempotency_key,sales_start_at,sales_end_at,created_by_id,notes,updated_at,created_at
  ) values(
    (p_input->>'seasonId')::integer,(p_input->>'countryId')::integer,v_region_id,v_code,btrim(p_input->>'name'),v_nature,v_status,p_input->>'idempotencyKey',
    nullif(p_input->>'startsAt','')::timestamptz,nullif(p_input->>'endsAt','')::timestamptz,(p_input->>'actorId')::integer,nullif(p_input->>'notes',''),now(),now()
  ) returning id into v_batch_id;
  v_line_status:=case when v_status='active' then 'active'::public.enum_qurbani_stock_batch_lines_status else 'draft'::public.enum_qurbani_stock_batch_lines_status end;

  for v_row in select value from jsonb_array_elements(p_input->'rows') loop
    select * into v_product from public.qurbani_products where id=(v_row->>'productId')::integer for update;
    if not found or v_product.season_id<>(p_input->>'seasonId')::integer or v_product.kind::text<>v_row->>'kind' then raise exception 'Stock product does not match season or kind'; end if;
    if v_product.country_id is not null and v_product.country_id<>(p_input->>'countryId')::integer then raise exception 'Product belongs to another country'; end if;
    if (v_row->>'animalCount')::integer<1 or (v_row->>'animalCount')::integer>500 then raise exception 'Animal count must be 1-500'; end if;
    if (v_row->>'capacity')::integer<1 or (v_row->>'capacity')::integer>7
      or (v_product.kind='small_livestock' and (v_row->>'capacity')::integer<>1) then raise exception 'Invalid animal capacity'; end if;
    if v_product.capacity<>(v_row->>'capacity')::integer then raise exception 'Product variant capacity does not match stock row capacity'; end if;
    if (v_row->>'price')::numeric<=0 or upper(v_row->>'currency') not in ('TRY','USD','EUR','GBP') then raise exception 'Invalid stock price'; end if;
    if v_product.funding_pool_id is null or v_product.campaign_id is null then raise exception 'Product campaign and funding pool are required'; end if;

    insert into public.qurbani_stock_batch_lines(
      batch_id,product_id,kind,animal_count,capacity,total_quantity,status,sales_start_at,sales_end_at,sort_order,updated_at,created_at
    ) values(
      v_batch_id,v_product.id,v_product.kind,(v_row->>'animalCount')::integer,(v_row->>'capacity')::integer,
      (v_row->>'animalCount')::integer*(v_row->>'capacity')::integer,v_line_status,
      nullif(v_row->>'salesStartAt','')::timestamptz,nullif(v_row->>'salesEndAt','')::timestamptz,coalesce((v_row->>'sortOrder')::integer,0),now(),now()
    ) returning id into v_line_id;
    select coalesce(max(revision),0)::integer+1 into v_revision from public.qurbani_price_revisions where product_id=v_product.id;
    update public.qurbani_price_revisions set status='retired',effective_to=coalesce(effective_to,now()),updated_at=now() where product_id=v_product.id and status='active';
    insert into public.qurbani_price_revisions(product_id,batch_line_id,revision,unit_price,currency,status,effective_from,created_by_id,reason,updated_at,created_at)
    values(v_product.id,v_line_id,v_revision,(v_row->>'price')::numeric,upper(v_row->>'currency')::public.enum_qurbani_products_currency,'active',now(),(p_input->>'actorId')::integer,'stock_batch_created',now(),now())
    returning id into v_revision_id;
    update public.qurbani_stock_batch_lines set price_revision_id=v_revision_id where id=v_line_id;
    update public.qurbani_products set country_id=(p_input->>'countryId')::integer,region_ref_id=v_region_id,current_price_revision_id=v_revision_id,
      price=(v_row->>'price')::numeric,currency=upper(v_row->>'currency')::public.enum_qurbani_products_currency,capacity=(v_row->>'capacity')::integer,updated_at=now() where id=v_product.id;
    for v_i in 1..(v_row->>'animalCount')::integer loop
      insert into public.qurbani_pools(season_id,product_id,country_id,stock_batch_line_id,price_revision_id,ordinal,capacity,reserved_count,confirmed_count,status,is_legacy,updated_at,created_at)
      values(v_product.season_id,v_product.id,(p_input->>'countryId')::integer,v_line_id,v_revision_id,v_i,(v_row->>'capacity')::integer,0,0,'open',false,now(),now());
    end loop;
    v_animal_count:=v_animal_count+(v_row->>'animalCount')::integer;
    v_total_capacity:=v_total_capacity+(v_row->>'animalCount')::integer*(v_row->>'capacity')::integer;
  end loop;
  update public.qurbani_stock_batches set animal_count=v_animal_count,total_capacity=v_total_capacity,available_capacity=v_total_capacity,updated_at=now() where id=v_batch_id;
  insert into public.audit_logs(action,actor_email,target_collection,target_id,details,updated_at,created_at)
  values('qurbani.stock_batch_created',nullif(p_input->>'actorEmail',''),'qurbani-stock-batches',v_batch_id::text,jsonb_build_object('animalCount',v_animal_count,'totalCapacity',v_total_capacity),now(),now());
  return jsonb_build_object('batchId',v_batch_id,'animalCount',v_animal_count,'totalCapacity',v_total_capacity,'availableCapacity',v_total_capacity,'idempotent',false);
end;
$$;

create or replace function private.qurbani_adjust_empty_stock(p_input jsonb)
returns jsonb
language plpgsql security invoker
set search_path=pg_catalog,public,private
as $$
declare v_batch public.qurbani_stock_batches%rowtype; v_line public.qurbani_stock_batch_lines%rowtype;
  v_delta integer:=(p_input->>'delta')::integer; v_i integer; v_max_ordinal integer; v_pool_id integer;
  v_animals integer; v_capacity integer; v_available integer;
begin
  if v_delta=0 or abs(v_delta)>500 then raise exception 'Stock delta must be between -500 and 500 and non-zero'; end if;
  select * into v_batch from public.qurbani_stock_batches where id=(p_input->>'batchId')::integer for update;
  if not found or v_batch.status in ('transferred','completed','archived') then raise exception 'Batch cannot be adjusted'; end if;
  if nullif(p_input->>'batchLineId','') is null then raise exception 'Stock batch line is required'; end if;
  select * into v_line from public.qurbani_stock_batch_lines
  where id=(p_input->>'batchLineId')::integer and batch_id=v_batch.id and status not in ('closed','cancelled')
  for update;
  if not found then raise exception 'Stock batch line not found in this batch'; end if;
  select coalesce(max(ordinal),0)::integer into v_max_ordinal from public.qurbani_pools where stock_batch_line_id=v_line.id;
  if v_delta>0 then
    for v_i in 1..v_delta loop
      insert into public.qurbani_pools(season_id,product_id,country_id,stock_batch_line_id,price_revision_id,ordinal,capacity,reserved_count,confirmed_count,status,is_legacy,operation_notes,updated_at,created_at)
      values(v_batch.season_id,v_line.product_id,v_batch.country_id,v_line.id,v_line.price_revision_id,v_max_ordinal+v_i,v_line.capacity,0,0,'open',false,nullif(p_input->>'reason',''),now(),now());
    end loop;
  else
    if nullif(btrim(coalesce(p_input->>'reason','')),'') is null then raise exception 'A reason is required to reduce stock'; end if;
    for v_pool_id in
      select id from public.qurbani_pools where stock_batch_line_id=v_line.id and status='open' and reserved_count=0 and confirmed_count=0 and code is null and locked_at is null
      order by ordinal desc,id desc for update skip locked limit -v_delta
    loop
      update public.qurbani_pools set status='withdrawn',operation_notes=p_input->>'reason',updated_at=now() where id=v_pool_id;
      v_i:=coalesce(v_i,0)+1;
    end loop;
    if coalesce(v_i,0)<>-v_delta then raise exception 'Not enough empty stock to withdraw'; end if;
  end if;
  select count(*)::integer,coalesce(sum(capacity),0)::integer
    into v_animals,v_capacity from public.qurbani_pools where stock_batch_line_id=v_line.id and status<>'withdrawn';
  update public.qurbani_stock_batch_lines
  set animal_count=v_animals,total_quantity=v_capacity,
      status=case
        when v_animals=0 then 'depleted'::public.enum_qurbani_stock_batch_lines_status
        when v_batch.status='active' then 'active'::public.enum_qurbani_stock_batch_lines_status
        else status
      end,
      updated_at=now()
  where id=v_line.id;
  select count(pool.id)::integer,coalesce(sum(pool.capacity),0)::integer,
         coalesce(sum(greatest(pool.capacity-pool.confirmed_count-pool.reserved_count,0)),0)::integer
    into v_animals,v_capacity,v_available
  from public.qurbani_stock_batch_lines line
  join public.qurbani_pools pool on pool.stock_batch_line_id=line.id and pool.status<>'withdrawn'
  where line.batch_id=v_batch.id;
  update public.qurbani_stock_batches
  set animal_count=v_animals,total_capacity=v_capacity,available_capacity=v_available,updated_at=now()
  where id=v_batch.id;
  insert into public.audit_logs(action,actor_email,target_collection,target_id,details,updated_at,created_at)
  values('qurbani.empty_stock_adjusted',nullif(p_input->>'actorEmail',''),'qurbani-stock-batch-lines',v_line.id::text,
    jsonb_build_object('batchId',v_batch.id,'delta',v_delta,'reason',p_input->>'reason'),now(),now());
  return jsonb_build_object('batchId',v_batch.id,'animalCount',v_animals,'totalCapacity',v_capacity,'availableCapacity',v_available);
end;
$$;

